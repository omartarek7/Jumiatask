package com.jumia.customers.models;

public class CountryInfo {

  private String country;
  private String code;
  private String regex;

  public CountryInfo() {
  }

  public String getCountry() {
    return country;
  }

  public String getCode() {
    return code;
  }

  public String getRegex() {
    return regex;
  }
}
